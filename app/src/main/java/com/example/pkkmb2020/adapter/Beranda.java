package com.example.pkkmb2020.adapter;

public class Beranda {

    private String nama;
    private int img;

    public Beranda() {
    }

    public Beranda(String nama, int img) {
        this.nama = nama;
        this.img = img;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }
}
